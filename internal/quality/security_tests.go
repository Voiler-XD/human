// generateSecurityTests produces a standalone bash script that uses curl to probe
// a running application for 7 vulnerability categories. Returns the script content
// and total test count.
func generateSecurityTests(app *ir.Application) (string, int) {
	if len(app.APIs) == 0 {
		return "", 0
	}

	var b strings.Builder
	count := 0

	writeScriptHeader(&b)
	writeHelperFunctions(&b)

	count += writeAuthBypassTests(&b, app)
	count += writeSQLInjectionTests(&b, app)
	count += writeXSSTests(&b, app)
	count += writeValidationBypassTests(&b, app)
	count += writeIDORTests(&b, app)
	count += writeRateLimitTests(&b, app)
	count += writeCORSTests(&b, app)

	writeScriptFooter(&b, count)

	return b.String(), count
}

// writeScriptHeader writes the shebang, argument parsing, and counters.
func writeScriptHeader(b *strings.Builder) {
	b.WriteString("#!/usr/bin/env bash\n")
	b.WriteString("# Security test script — generated by Human compiler\n")
	b.WriteString("# Usage: ./security-tests.sh <BASE_URL> [AUTH_TOKEN]\n")
	b.WriteString("set -euo pipefail\n\n")
	b.WriteString("BASE_URL=\"${1:?Usage: $0 <BASE_URL> [AUTH_TOKEN]}\"\n")
	b.WriteString("AUTH_TOKEN=\"${2:-}\"\n")
	b.WriteString("PASS=0\nFAIL=0\nTOTAL=0\n\n")
}

// writeHelperFunctions writes the assert_status and assert_not_status bash helpers.
func writeHelperFunctions(b *strings.Builder) {
	b.WriteString("assert_status() {\n")
	b.WriteString("  local desc=\"$1\" expected=\"$2\" actual=\"$3\"\n")
	b.WriteString("  TOTAL=$((TOTAL + 1))\n")
	b.WriteString("  if [ \"$actual\" -eq \"$expected\" ]; then\n")
	b.WriteString("    PASS=$((PASS + 1))\n")
	b.WriteString("    echo \"  PASS: $desc\"\n")
	b.WriteString("  else\n")
	b.WriteString("    FAIL=$((FAIL + 1))\n")
	b.WriteString("    echo \"  FAIL: $desc (expected $expected, got $actual)\"\n")
	b.WriteString("  fi\n")
	b.WriteString("}\n\n")

	b.WriteString("assert_not_status() {\n")
	b.WriteString("  local desc=\"$1\" rejected=\"$2\" actual=\"$3\"\n")
	b.WriteString("  TOTAL=$((TOTAL + 1))\n")
	b.WriteString("  if [ \"$actual\" -ne \"$rejected\" ]; then\n")
	b.WriteString("    PASS=$((PASS + 1))\n")
	b.WriteString("    echo \"  PASS: $desc\"\n")
	b.WriteString("  else\n")
	b.WriteString("    FAIL=$((FAIL + 1))\n")
	b.WriteString("    echo \"  FAIL: $desc (got rejected status $rejected)\"\n")
	b.WriteString("  fi\n")
	b.WriteString("}\n\n")
}

// writeScriptFooter writes the summary and exit code.
func writeScriptFooter(b *strings.Builder, count int) {
	b.WriteString("# ── Summary ──\n\n")
	b.WriteString("echo \"\"\n")
	fmt.Fprintf(b, "echo \"Security Tests: $TOTAL total, $PASS passed, $FAIL failed (%d probes)\"\n", count)
	b.WriteString("if [ \"$FAIL\" -gt 0 ]; then\n")
	b.WriteString("  exit 1\n")
	b.WriteString("fi\n")
}

// writeAuthBypassTests probes auth-protected endpoints without a token and with a malformed token.
func writeAuthBypassTests(b *strings.Builder, app *ir.Application) int {
	count := 0
	authEndpoints := []*ir.Endpoint{}
	for _, ep := range app.APIs {
		if ep.Auth {
			authEndpoints = append(authEndpoints, ep)
		}
	}
	if len(authEndpoints) == 0 {
		return 0
	}

	b.WriteString("# ── 1. Auth Bypass ──\n")
	b.WriteString("echo \"[1/7] Auth Bypass\"\n\n")

	for _, ep := range authEndpoints {
		method := strings.ToUpper(httpMethod(ep.Name))
		path := apiPath(ep.Name)

		// Test without auth token
		fmt.Fprintf(b, "STATUS=$(curl -s -o /dev/null -w '%%{http_code}' -X %s \"$BASE_URL%s\")\n", method, path)
		desc := fmt.Sprintf("%s %s without auth token", method, path)
		fmt.Fprintf(b, "assert_status '%s' 401 \"$STATUS\"\n\n", shellEscapeSingleQuote(desc))
		count++

		// Test with malformed token
		fmt.Fprintf(b, "STATUS=$(curl -s -o /dev/null -w '%%{http_code}' -X %s -H 'Authorization: Bearer invalid-token-xxx' \"$BASE_URL%s\")\n", method, path)
		desc = fmt.Sprintf("%s %s with malformed token", method, path)
		fmt.Fprintf(b, "assert_status '%s' 401 \"$STATUS\"\n\n", shellEscapeSingleQuote(desc))
		count++
	}

	return count
}

// writeSQLInjectionTests sends SQL injection payloads to text parameters.
func writeSQLInjectionTests(b *strings.Builder, app *ir.Application) int {
	count := 0
	payloads := []string{
		"' OR '1'='1",
		"' UNION SELECT null--",
		"'; DROP TABLE users;--",
	}

	type textParam struct {
		ep    *ir.Endpoint
		param string
	}
	var targets []textParam
	for _, ep := range app.APIs {
		for _, p := range ep.Params {
			if isTextField(app, p.Name) {
				targets = append(targets, textParam{ep, p.Name})
			}
		}
	}
	if len(targets) == 0 {
		return 0
	}

	b.WriteString("# ── 2. SQL Injection ──\n")
	b.WriteString("echo \"[2/7] SQL Injection\"\n\n")

	for _, t := range targets {
		method := strings.ToUpper(httpMethod(t.ep.Name))
		path := apiPath(t.ep.Name)

		for _, payload := range payloads {
			body := curlJSONBody(map[string]string{t.param: payload})
			authHeader := ""
			if t.ep.Auth {
				authHeader = " -H 'Authorization: Bearer '\"$AUTH_TOKEN\""
			}
			fmt.Fprintf(b, "STATUS=$(curl -s -o /dev/null -w '%%{http_code}' -X %s%s -H 'Content-Type: application/json' -d '%s' \"$BASE_URL%s\")\n",
				method, authHeader, shellEscapeSingleQuote(body), path)
			desc := fmt.Sprintf("SQLi on %s %s param %s", method, path, t.param)
			fmt.Fprintf(b, "assert_not_status '%s' 200 \"$STATUS\"\n\n", shellEscapeSingleQuote(desc))
			count++
		}
	}

	return count
}

